package com.nxt.model;

/**
 * The Class ItemOutput.
 */
public class ItemOutput {
	
	/** The id. */
	private String id;
	
	/** The type. */
	private String type;
	
	/** The name. */
	private String name;
	
	/** The batter. */
	private String batter;
	
	/** The topping. */
	private String topping;
	
	/**
	 * Gets the id.
	 *
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	
	/**
	 * Sets the id.
	 *
	 * @param id the new id
	 */
	public void setId(String id) {
		this.id = id;
	}
	
	/**
	 * Gets the type.
	 *
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	
	/**
	 * Sets the type.
	 *
	 * @param type the new type
	 */
	public void setType(String type) {
		this.type = type;
	}
	
	/**
	 * Gets the name.
	 *
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	
	/**
	 * Sets the name.
	 *
	 * @param name the new name
	 */
	public void setName(String name) {
		this.name = name;
	}
	
	/**
	 * Gets the batter.
	 *
	 * @return the batter
	 */
	public String getBatter() {
		return batter;
	}
	
	/**
	 * Sets the batter.
	 *
	 * @param batter the new batter
	 */
	public void setBatter(String batter) {
		this.batter = batter;
	}
	
	/**
	 * Gets the topping.
	 *
	 * @return the topping
	 */
	public String getTopping() {
		return topping;
	}
	
	/**
	 * Sets the topping.
	 *
	 * @param topping the new topping
	 */
	public void setTopping(String topping) {
		this.topping = topping;
	}

}
